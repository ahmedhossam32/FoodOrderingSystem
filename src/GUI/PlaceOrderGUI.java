 private void populateRestaurants() {
    restaurantList = db.getAllRestaurants();

    System.out.println("Found restaurants: " + restaurantList.size());
    for (Restaurant r : restaurantList) {
        System.out.println("- " + r.getName()); // DEBUGGING LINE
        jComboBox1.addItem(r.getName());
    }

    jComboBox1.repaint(); // just to force UI to update

    if (restaurantList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "âŒ No restaurants found in the system.");
        jButton2.setEnabled(false); 
    }
}


    private void viewMenuAction() {
    int selectedIndex = jComboBox1.getSelectedIndex();

    if (selectedIndex >= 0) {
        Restaurant selectedRestaurant = restaurantList.get(selectedIndex);
        new MenuGUI(client, selectedRestaurant).setVisible(true); // you'll create MenuGUI
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Please select a restaurant first.");
    }
}

   
