    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    String cardNumber = jTextField1.getText().trim();
   
    String cvc = jTextField3.getText().trim();
    String expiryDate = jTextField4.getText().trim();

    CreditCard strategy = new CreditCard(cardNumber, expiryDate, cvc);

    // Create payment
    Payment payment = new Payment(order.calculateTotal(), strategy, order, client);

    if (payment.processPayment(order)) {
        db.addPayment(payment); // ✅ Save to DB
        JOptionPane.showMessageDialog(this, "✅ Payment Successful!\nPayment ID: " + payment.getPaymentId());
        new ClientDashboardGUI(client).setVisible(true); // Go to Home
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "❌ Payment failed. Please check your card info.");
    }

    }//GEN-LAST:event_jButton1ActionPerformed

    
