    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String email = jTextField1.getText().trim();
    String password = new String(jPasswordField1.getPassword()).trim();

    if (email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both email and password.", "Input Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    handleClientLogin(email, password);
    handleAdminLogin(email, password);
    handleDeliveryLogin(email, password);

    // If no role matched, show error
    if (!userFound) {
        JOptionPane.showMessageDialog(this, "Invalid credentials! Please try again.", "Login Failed", JOptionPane.ERROR_MESSAGE);
    
    }
    }//GEN-LAST:event_jButton1ActionPerformed

