    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
    String method = jComboBox1.getSelectedItem().toString();

    switch (method) {
        case "Credit Card":
            new CreditCardGUI(client, order).setVisible(true);
            break;
       
             case "Cash":
            // 1. Show payment amount
            double totalAmount = order.calculateTotal();
            JOptionPane.showMessageDialog(this, 
                "Please pay the amount due at the restaurant:\n" + totalAmount + " $");
            Cash cashStrategy = new Cash(totalAmount);
          
            Payment payment = new Payment(totalAmount, cashStrategy, order, client);
            boolean success = payment.processPayment(order);

            if (success) {
                
                db.addPayment(payment);
                JOptionPane.showMessageDialog(this, "✅ Payment recorded successfully.");
                new ClientDashboardGUI(client).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "❌ Payment failed.");
            }
            break;
        case "InstaPay":
            new InstaPayGUI2(client, order).setVisible(true);
            break;
        default:
            JOptionPane.showMessageDialog(this, "❌ Invalid payment method.");
            return;
    }

    this.dispose(); 

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        int orderId = Integer.parseInt(jTextField1.getText().trim());
        Order foundOrder = db.getOrderById(orderId);

        if (foundOrder != null && foundOrder.getClient().getClientID() == client.getClientID()) {
            this.order = foundOrder; // update current order object
            jLabel3.setText(order.calculateTotal() + " $");
        } else {
            JOptionPane.showMessageDialog(this, "❌ Order not found or does not belong to you.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Invalid Order ID.");
    }


    }//GEN-LAST:event_jButton3ActionPerformed

