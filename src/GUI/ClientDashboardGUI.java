  private void loadNotifications() {
    Database db = new Database();
    List<String> list = db.getUnreadNotifications(client.getClientID());

    if (!list.isEmpty()) {
        for (String msg : list) {
            jTextArea1.append( "Hey " + client.getName() +" your " + msg + "\n");
            JOptionPane.showMessageDialog(this,"Hey " + client.getName() + " your "+ msg);
        }
        db.markNotificationsAsRead(client.getClientID());
    }
}


    
    
