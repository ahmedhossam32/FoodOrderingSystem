    private void handleSignup() {
    String name = jTextField3.getText().trim();
    String address = jTextField5.getText().trim();
    String username = jTextField4.getText().trim(); // Not used in constructor, but collected
    String email = jTextField1.getText().trim();
    String password = jTextField2.getText().trim();
    String confirmPassword = new String(jPasswordField1.getPassword()).trim();

    // Validate fields
    if (name.isEmpty() || address.isEmpty() || email.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Missing Info", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (!password.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.", "Password Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

   
    int phoneNumber = 100000000 + new Random().nextInt(900000000);

    String gender = jComboBox1.getSelectedItem().toString();

    
    Client client = new Client(name, gender, email, password, phoneNumber, address);
    Database db = new Database();
    db.addClient(client);

    JOptionPane.showMessageDialog(this, "âœ… Signup successful. You can now login.");
    this.dispose();
    new LoginGUI2().setVisible(true);
}

  
